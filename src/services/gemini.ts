import { GoogleGenAI } from "@google/genai";

// Initialize the Gemini API client using the environment variable.
// In AI Studio, GEMINI_API_KEY is automatically injected.
const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY });

export async function getCuratorResponse(message: string, history: { role: 'user' | 'model', parts: { text: string }[] }[]) {
  if (!process.env.GEMINI_API_KEY) {
    return "ИИ-Куратор временно недоступен. (API Key missing)";
  }

  try {
    const response = await ai.models.generateContent({
      model: "gemini-3-flash-preview",
      contents: history.concat([{ role: 'user', parts: [{ text: message }] }]),
      config: {
        systemInstruction: "Вы — ИИ-Куратор пути индивидуации, мудрый юнгианский аналитик. Вы сопровождаете женщин 35–45 лет в прохождении курса по аналитической психологии Юнга. Вы знаете теорию индивидуации, Тени, Персоны, Анимы/Анимуса и Самости. Ваш тон — тёплый, глубокий, поддерживающий, психологически грамотный. Вы говорите языком жизни, а не учебника. Вы отвечаете на вопросы о пути, помогаете интерпретировать сложные моменты, поддерживаете в трудных местах. Задавайте вопросы и расширяйте перспективу. Обращайтесь к пользователю строго на «Вы» во множественном числе. Отвечайте на русском языке.",
      },
    });

    return response.text || "Я задумался над вашим вопросом...";
  } catch (error) {
    console.error("Gemini Chat Error:", error);
    return "Связь с ИИ-Куратором прервалась, попробуйте позже.";
  }
}

export async function getJungianAnalysis(content: string, type: string) {
  if (!process.env.GEMINI_API_KEY) {
    return "ИИ-Куратор временно недоступен. (API Key missing)";
  }

  const prompts: Record<string, string> = {
    'reflection': `Вы — ИИ-Куратор пути индивидуации, юнгианский аналитик. Женщина написала рефлексию в рамках курса по Юнгу. Дайте тёплую, глубокую обратную связь: что вы видите в этой записи? Какие движения психики заметны? Задайте один вопрос, который поможет ей пойти глубже. Обращайтесь к ней на «Вы». Запись: "${content}"`,
    'projection': `Вы — ИИ-Куратор пути индивидуации. Женщина работает с проекциями — юнгианским методом работы с Тенью. Помогите ей увидеть, что именно она проецирует и как это связано с её собственной непознанной частью. Будьте мягкой, но честной. Обращайтесь к ней на «Вы». Запись: "${content}"`,
    'dreams': `Вы — ИИ-Куратор пути индивидуации. Женщина описала сон или образ из бессознательного. Используйте юнгианский метод амплификации: расширьте символику образа, найдите архетипические параллели. Не интерпретируйте директивно — предложите несколько возможных смыслов и задайте вопрос. Обращайтесь к ней на «Вы». Запись: "${content}"`,
    'imagination': `Вы — ИИ-Куратор пути индивидуации. Женщина выполнила практику активного воображения — прямой диалог с образом бессознательного. Отметьте, что важного произошло в этом диалоге. Какой архетип мог проявиться? Что это говорит о её пути? Обращайтесь к ней на «Вы». Запись: "${content}"`
  };

  try {
    const response = await ai.models.generateContent({
      model: "gemini-3-flash-preview",
      contents: prompts[type] || `Проанализируйте эту запись с точки зрения юнгианской психологии: "${content}"`,
      config: {
        systemInstruction: "Вы — ИИ-Куратор Дерева Самости, мудрый и современный юнгианский аналитик. Ваш тон — интеллектуальный, эстетичный, эмпатичный. Вы помогаете женщинам 35-45 лет в процессе самопознания. Отвечайте на русском языке. Используйте термины: архетип, проекция, индивидуация, но объясняйте их доступно.",
      },
    });

    return response.text || "Мысли пока не оформились в слова.";
  } catch (error) {
    console.error("Gemini Error:", error);
    return "ИИ-Куратор временно недоступен, попробуйте позже.";
  }
}
